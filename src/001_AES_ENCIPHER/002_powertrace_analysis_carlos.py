def decrypt_text(ciphertext, key):
    padded_text = ciphertext + (16 - len(ciphertext) % 16) * b' '

    cipher = Cipher(algorithms.AES(key), modes.ECB(), backend=default_backend())
    decryptor = cipher.decryptor()

    cleartext = decryptor.update(padded_text) + decryptor.finalize()
    return base64.b64encode(cleartext)


